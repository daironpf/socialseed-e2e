class WindowsSafeConsole:
    """Wrapper around Console that replaces Unicode on Windows legacy consoles."""
    
    def __init__(self, console: Console):
        self._console = console
    
    def _sanitize_string(self, text: str) -> str:
        """Replace Unicode characters with ASCII alternatives."""
        for char, replacement in UNICODE_REPLACEMENTS.items():
            text = text.replace(char, replacement)
        return text
    
    def print(self, *args, **kwargs):
        """Print with Unicode replacement on Windows."""
        if IS_WINDOWS:
            processed_args = []
            for arg in args:
                if isinstance(arg, str):
                    arg = self._sanitize_string(arg)
                elif isinstance(arg, Exception):
                    # Handle exceptions - sanitize the exception message
                    arg = self._sanitize_string(str(arg))
                processed_args.append(arg)
            self._console.print(*processed_args, **kwargs)
        else:
            self._console.print(*args, **kwargs)
    
    def __getattr__(self, name):
        """Delegate all other attributes to the underlying console."""
        return getattr(self._console, name)


def get_console() -> WindowsSafeConsole:
    """Get a Console instance configured for the current platform.
    
    On Windows legacy consoles (cmd.exe, PowerShell), emojis may not display
    properly. This function creates a Console that handles this gracefully
    by wrapping it with emoji replacement support.
    
    Returns:
        WindowsSafeConsole: A wrapped Rich Console instance for Windows.
    """
    if IS_WINDOWS:
        # Create a basic Console, Rich will auto-detect the best mode
        inner_console = Console(
            force_terminal=True,
            legacy_windows=False,  # Disable legacy mode to avoid encoding errors
        )
        return WindowsSafeConsole(inner_console)
    else:
        inner_console = Console()
        return WindowsSafeConsole(inner_console)


